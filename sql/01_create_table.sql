CREATE TABLE hr (
    emp_id VARCHAR(20),
    first_name VARCHAR(50),
    last_name VARCHAR(50),
    birthdate VARCHAR(20),
    gender VARCHAR(20),
    race VARCHAR(100),
    department VARCHAR(100),
    jobtitle VARCHAR(100),
    location VARCHAR(100),
    hire_date VARCHAR(20),
    termdate VARCHAR(50),
    location_city VARCHAR(100),
    location_state VARCHAR(100)
);